package org.glavo.javafx.webp;

import org.junit.jupiter.api.Test;

import java.io.ByteArrayInputStream;
import java.nio.ByteBuffer;

import static org.junit.jupiter.api.Assertions.assertEquals;

/// Ports of [decoder.rs](https://github.com/image-rs/image-webp/blob/f4d80bd965df2c81e65b6f43c1f70e0750bd4b0f/src/decoder.rs) unit tests.
final class ImageWebpDecoderPortedTest {

    @Test
    void addWithOverflowSizeDoesNotThrowUnexpectedExceptions() throws Exception {
        byte[] bytes = {
                0x52, 0x49, 0x46, 0x46, (byte) 0xAF, 0x37, (byte) 0x80, 0x47, 0x57, 0x45, 0x42, 0x50, 0x6C, 0x64,
                0x00, 0x00, (byte) 0xFF, (byte) 0xFF, (byte) 0xFF, (byte) 0xFF, (byte) 0xFB, 0x7E, 0x73, 0x00, 0x06, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00, 0x65, 0x65, 0x65, 0x65, 0x65, 0x65,
                0x40, (byte) 0xFB, (byte) 0xFF, (byte) 0xFF, 0x65, 0x65, 0x65, 0x65, 0x65, 0x65, 0x65, 0x65, 0x65, 0x65,
                0x00, 0x00, 0x00, 0x00, 0x62, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x49,
                0x49, 0x54, 0x55, 0x50, 0x4C, 0x54, 0x59, 0x50, 0x45, 0x33, 0x37, 0x44, 0x4D, 0x46
        };

        try (WebPImageReader ignored = WebPDecoder.open(new ByteArrayInputStream(bytes))) {
            // Success is acceptable. Upstream only checks that construction does not panic.
        } catch (WebPException ignored) {
            // Decoder rejection is also acceptable as long as it remains a controlled failure.
        }
    }

    @Test
    void decode2x2SingleColorImage() throws Exception {
        byte[] bytes = {
                0x52, 0x49, 0x46, 0x46, 0x3C, 0x00, 0x00, 0x00, 0x57, 0x45, 0x42, 0x50, 0x56, 0x50,
                0x38, 0x20, 0x30, 0x00, 0x00, 0x00, (byte) 0xD0, 0x01, 0x00, (byte) 0x9D, 0x01, 0x2A, 0x02, 0x00,
                0x02, 0x00, 0x02, 0x00, 0x34, 0x25, (byte) 0xA0, 0x02, 0x74, (byte) 0xBA, 0x01, (byte) 0xF8, 0x00, 0x03,
                (byte) 0xB0, 0x00, (byte) 0xFE, (byte) 0xF0, (byte) 0xC4, 0x0B, (byte) 0xFF, 0x20, (byte) 0xB9, 0x61, 0x75, (byte) 0xC8, (byte) 0xD7, (byte) 0xFF,
                0x20, 0x3F, (byte) 0xE4, 0x07, (byte) 0xFC, (byte) 0x80, (byte) 0xFF, (byte) 0xF8, (byte) 0xF2, 0x00, 0x00, 0x00
        };

        WebPImage image = WebPDecoder.decodeAll(new ByteArrayInputStream(bytes));
        assertUniformOpaque(image.getFrames().get(0), 2, 2);
    }

    @Test
    void decode3x3SingleColorImage() throws Exception {
        byte[] bytes = {
                0x52, 0x49, 0x46, 0x46, 0x3C, 0x00, 0x00, 0x00, 0x57, 0x45, 0x42, 0x50, 0x56, 0x50,
                0x38, 0x20, 0x30, 0x00, 0x00, 0x00, (byte) 0xD0, 0x01, 0x00, (byte) 0x9D, 0x01, 0x2A, 0x03, 0x00,
                0x03, 0x00, 0x02, 0x00, 0x34, 0x25, (byte) 0xA0, 0x02, 0x74, (byte) 0xBA, 0x01, (byte) 0xF8, 0x00, 0x03,
                (byte) 0xB0, 0x00, (byte) 0xFE, (byte) 0xF0, (byte) 0xC4, 0x0B, (byte) 0xFF, 0x20, (byte) 0xB9, 0x61, 0x75, (byte) 0xC8, (byte) 0xD7, (byte) 0xFF,
                0x20, 0x3F, (byte) 0xE4, 0x07, (byte) 0xFC, (byte) 0x80, (byte) 0xFF, (byte) 0xF8, (byte) 0xF2, 0x00, 0x00, 0x00
        };

        WebPImage image = WebPDecoder.decodeAll(new ByteArrayInputStream(bytes));
        assertUniformOpaque(image.getFrames().get(0), 3, 3);
    }

    private static void assertUniformOpaque(WebPFrame frame, int width, int height) {
        assertEquals(width, frame.getWidth());
        assertEquals(height, frame.getHeight());

        byte[] pixels = readPixels(frame);
        assertEquals(width * height * 4, pixels.length);

        int r = pixels[0] & 0xFF;
        int g = pixels[1] & 0xFF;
        int b = pixels[2] & 0xFF;
        int a = pixels[3] & 0xFF;
        assertEquals(0xFF, a);

        for (int i = 0; i < pixels.length; i += 4) {
            assertEquals(r, pixels[i] & 0xFF, "red channel at pixel " + (i / 4));
            assertEquals(g, pixels[i + 1] & 0xFF, "green channel at pixel " + (i / 4));
            assertEquals(b, pixels[i + 2] & 0xFF, "blue channel at pixel " + (i / 4));
            assertEquals(a, pixels[i + 3] & 0xFF, "alpha channel at pixel " + (i / 4));
        }
    }

    private static byte[] readPixels(WebPFrame frame) {
        ByteBuffer pixels = frame.getPixels();
        byte[] bytes = new byte[pixels.remaining()];
        pixels.get(bytes);
        return bytes;
    }
}
